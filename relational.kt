fun main() {
	var num1 = 47
	var num2 = 16
	
	println("Values are:"+num1+", "+num2)
	println("Less Than:" + (num1 < num2))
	println("Less Than/Equal:" + (num1 <= num2))
	println("Greater Than:" + (num1 > num2))
	println("Greater Than/Equal:" + (num1 >= num2))
	println("Equal:" + (num1 == num2))
	println("Not Equal:" + (num1 != num2))
}
